    public void fillDataTable(){
        DefaultTableModel model = (DefaultTableModel) jTable_diem.getModel();
        model.setRowCount(0);
        for (Diem diem : dDAO.getAllDiemSV()) {
            Object[] newData = new Object[7];
            newData[0] = diem.getSv().getMaSV();
            newData[1] = diem.getSv().getTenSV();
            newData[2] = diem.getdToan();
            newData[3] = diem.getdLi();
            newData[4] = diem.getdHoa();
            newData[5] = diem.diemTBC();
            newData[6] = diem.xepLoai();
            model.addRow(newData);
        }
    }
    public boolean ValidateForm(){
        if(jTextField1.getText().isEmpty()
                || jTextField2.getText().isEmpty()
                || jTextField3.getText().isEmpty()
                || jTextField4.getText().isEmpty()
                || jTextField5.getText().isEmpty()){
            return false;
        }else{
            try {
                double toan = Double.parseDouble(jTextField3.getText());
                double li = Double.parseDouble(jTextField4.getText());
                double hoa = Double.parseDouble(jTextField5.getText());
                if((toan <0 || toan >10) && (li<0 || li>10) && (hoa<0 || hoa >10)){
                    return false;
                }
            } catch (Exception e) {
                return false;
            }
        }
        return true;              
    }
    public Diem getModel(){
        Diem diem = new Diem();
        diem.setId(0);
        SinhVien sv = new SinhVien(jTextField1.getText(), jTextField2.getText());
        diem.setSv(sv);
        diem.setdToan(Double.parseDouble(jTextField3.getText()));
        diem.setdLi(Double.parseDouble(jTextField4.getText()));
        diem.setdHoa(Double.parseDouble(jTextField5.getText()));
        diem.setTbc(Double.parseDouble(jLabel_DiemTBC.getText()));
        
        return diem;
       
    }
    public void setModel(Diem d){
        jTextField1.setText(d.getSv().getMaSV());
        jTextField2.setText(d.getSv().getTenSV());
        jTextField3.setText(String.valueOf(d.getdToan()));
        jTextField4.setText(String.valueOf(d.getdLi()));
        jTextField5.setText(String.valueOf(d.getdHoa()));
        jLabel_DiemTBC.setText(String.valueOf(d.diemTBC()));
    }
