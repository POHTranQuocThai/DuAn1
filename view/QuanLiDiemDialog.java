    private void jButton_luuDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_luuDActionPerformed
        // TODO add your handling code here:
        if(ValidateForm()){
            Diem d = getModel();
            if(dDAO.plus(d)>0){
                fillDataTable();
            }
        }else {
            JOptionPane.showMessageDialog(this, "Vui lòng kiểm tra thông tin!");
        }
    }//GEN-LAST:event_jButton_luuDActionPerformed

    private void jButton_suaDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_suaDActionPerformed
        // TODO add your handling code here:
        if(ValidateForm()){
            Diem diem = getModel();
            if(dDAO.updateDiemSV(diem)>0){
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                fillDataTable();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin");
        }
    }//GEN-LAST:event_jButton_suaDActionPerformed

    private void jButton_xoaDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_xoaDActionPerformed
        // TODO add your handling code here:
        if(ValidateForm()){
            if(dDAO.delDiemSV(jTextField1.getText())>0){
                fillDataTable();
            }
        }else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin");
        }
    }//GEN-LAST:event_jButton_xoaDActionPerformed

    private void jButton_timActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_timActionPerformed
        // TODO add your handling code here:
        if(jTextField_timMSV.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Sinh viên cần tìm");
        }else {
            SinhVien sv = svDao.getSinhVienByID(jTextField_timMSV.getText());
            if(sv!=null){
                Diem d = dDAO.getDiemByMaSV(sv.getMaSV());
                if(d!= null){
                    setModel(d);
                }
                else{
                jTextField1.setText(sv.getMaSV());
                jTextField2.setText(sv.getTenSV());
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jLabel_DiemTBC.setText("0.0");
                }
            }                                                                                                           
        }
    }//GEN-LAST:event_jButton_timActionPerformed
    
    private void jTable_diemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_diemMouseClicked
        // TODO add your handling code here:
        int id = jTable_diem.rowAtPoint(evt.getPoint());
        String maSV = jTable_diem.getValueAt(id, 0).toString();
        Diem d = dDAO.getDiemByMaSV(maSV);
        setModel(d);
    }//GEN-LAST:event_jTable_diemMouseClicked

